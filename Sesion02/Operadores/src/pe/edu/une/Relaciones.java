package pe.edu.une;

public class Relaciones {
	public static void main(String[] args) {
		// Datos
		int v1 = 25;
		int v2 = 10;
		int v3 = 10;
		
		//Proceso y reporte
		System.out.println("풴1 es igual a V2?: "+(v1==v2));
		System.out.println("풴1 es diferente a V2?: "+(v1!=v2));
		System.out.println("풴3 es menor que V1?: "+(v3<v1));
		System.out.println("풴3 es mayor que V2?: "+(v3>v2));
		System.out.println("풴2 es menor igual que V1?: "+(v2<=v1));
		System.out.println("풴1 es mayor igual que V3?: "+(v1>=v3));
	}

}
